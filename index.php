        <div id="why" class="section">
            <div class="two-thirds left">
                <h2 class="underline leftline"><span style="color: #f8961d;">Why</span> do we help refugees?</h2>
                <p>
                    At Refugee Connect we believe that every person should experience love and acceptance,
                    regardless of their ethnic background. This is important because there are people
                    living in our community who, because of their ethnic background, are not experiencing
                    this love and acceptance.
                </p>
                <p>
                    If refugee families aren’t supported they disengage and find themselves in distress.
                    We’ve found that this regularly starts about 8 – 10 months after they arrive and
                    depending upon their circumstances, it can be more than 5 years before they begin to
                    look at life clearly again. We have found that those who have more connections with
                    different parts of the community contribute more positively into society.
                </p>
                <p>
                    Having fled war and persecution they need care and compassion to help them heal.
                </p>
