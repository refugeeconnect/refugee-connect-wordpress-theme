            <div class="two-thirds right">
                <h2 class="underline leftline"><span style="color: #f8961d;">Our position</span> about people seeking asylum in Australia.</h2>
                <p>
                    At Refugee Connect we believe that every person needs to know and
                    experience love, compassion and acceptance, regardless of their ethnic
                    background; that all people deserve to live in peace. That’s why we
                    strive to treat others the way we’d want to be treated and seek to be
                    good neighbours to others in time of need. If any one of us feared for
                    our life or for the lives of our family we’d like to know that others –
                    even complete strangers – would help us to safety, not turn us back into
                    harm. Too often, whole families escape war and survive treacherous journeys,
                    only to be denied any rights in a community where they ought to be safe.
                    Across our globe, people are confronting unimaginable horrors and risking
                    everything in the hope of reaching safety.
                </p>
                <p>
                    Australia must create a fair and efficient asylum process so we can show that,
                    when people are in harm’s way, we’ll do the right thing to determine who
                    requires safe haven. By considering each person’s case with dignity and respect,
                    we’ll set an example so that lucky countries like ours can follow our lead and
                    treat fellow human beings with compassion and care.
                </p>
                <p>
                    We cannot allow any abuse against people in detention centres. Nor can we turn
                    an issue of human rights into political bickering. This is not a matter of
                    political left or right, but quite simply a matter of right and wrong.
                </p>
                <p>
                    To make sure everyone can flourish, we need to fairly examine each person’s
                    asylum case in a safe place and the people who require safety must be quickly
                    integrated into our communities so these people can get on with rebuilding their lives.
                </p>
                <small>
                    <em>
                        We have adopted this statement that sets out our position about people seeking
                        asylum in Australia having considered research conducted by the Asylum Seeker
                        Research Centre in 2015.
                    </em>
                </small>
